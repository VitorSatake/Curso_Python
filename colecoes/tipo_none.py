"""
Tipo None

O tipo None em python representa o tipo sem tipo, ou poderia ser conhecido tambem como 
tipo vazio, porem, falar que é um tipo sem tipo é mais apropriado.

OBS : O tipo None é sempre especificado com a primeira letra maiuscula

Certo : None
Errado : none

Quando utilizamos o tipo None?

Podemos utilizar None quando queremos criar uma variavel e inicializa-la com um tipo sem tipo,
antes de recebermos um valor final.

OBS : O tipo None em python é sempre considerado como False
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 3.45, 2.54
print(numeros)
print(type(numeros))